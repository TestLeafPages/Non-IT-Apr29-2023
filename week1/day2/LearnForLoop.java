package week1.day2;

public class LearnForLoop {

	public static void main(String[] args) {
		
		//1 to 10
		//for-ctrl+space -choose 2nd option
		for (int i = 1; i <=10; i++) {
			//1---i++ (1+1)
			//11<=10
			System.out.println(i);
		}
		
		System.out.println("-------------------------------");
		
		
		for (int i = 10; i>=1; i--) {
			System.out.println(i);
		}
	}

}
